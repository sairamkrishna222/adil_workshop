$(document).ready(function(){
    $('ul li:nth-child(1)').on({
      mouseenter: function(){
        $(this).css("background-color", "red");
      },  
      mouseleave: function(){
        $(this).css("background-color", "lightblue");
      }, 
      click: function(){
        $(this).css("background-color", "yellow");
      }  
    });
});
